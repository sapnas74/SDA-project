package sdaakademija.fundamentals.objectoriented.room;

public class Room {

    private int width;
    private int length;

    public Room(int width , int length) {
        this.width = width;
        this.length = length;

    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }

}
